CREATE TABLE TOPIC(
	ID INT GENERATED BY DEFAULT AS IDENTITY(START WITH 1) PRIMARY KEY,
	LABEL VARCHAR(200)
)

CREATE TABLE USER(
	ID INT GENERATED BY DEFAULT AS IDENTITY(START WITH 1) PRIMARY KEY,
	USERNAME VARCHAR(200),
	EMAIL VARCHAR(200)
)

CREATE TABLE SUBSCRIPTION (
	TOPIC_ID INT,
	USER_ID INT,
	FOREIGN KEY(TOPIC_ID) REFERENCES TOPIC (ID),
	FOREIGN KEY(USER_ID) REFERENCES USER (ID)
)